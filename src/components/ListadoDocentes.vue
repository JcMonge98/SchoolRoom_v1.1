<script>
import UserService from "../services/user.service";
export default {
  data: () => ({
    correo: { nombres: "junito", apellidos: "perez" },
    buscar: "",
    buscar2: "",
    dialog: "",
    lazy: "",
    nombres: "",
    apellidos: "",
    roles: "",
    email: "",
    password: "",
    confirmar: "",
    RolTexto: [(v) => !!v || "Este campo es requerido",
      (v) => /[a-zA-Z]\d*/.test(v) || "No se permiten números",
    ],
    Rolemail: [
      (v) => !!v || "Correo es requerido",
      (v) => /.+@.+\..+/.test(v) || "E-mail no valido",
    ],
    Rolpassword: [(v) => !!v || "Contraseña es requerida"],
    RolconPassword: [(v) => !!v || "Contraseña es requerida"],
    //columnas de la tabla
    headers: [
      /* {
        text: "Codigo",
        align: "start",
        sortable: false,
        value: "_id"
      },*/
      {
        text: "Nombres",
        sortable: false,
        value: "nombres",
      },
      {
        text: "Apellidos",
        sortable: false,
        value: "apellidos",
      },
      {
        text: "E-mail",
        sortable: false,
        value: "email",
      },
      {
        text: "Password",
        sortable: false,
        value: "password",
      },
      {
        text: "Rol",
        sortable: false,
        value: "role.nombre",
      },
